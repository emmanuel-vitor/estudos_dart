
main() {
  /*
  For: é um função, com escopo proprio, onde determinamos o seu inicio,
  e até onde se deseja chegar. Tem o "pontei" que guia as suas condições;
  *são tres partes principais:
  for(variavel; condição; incremento) */

  for (var i = 0; i < 10; i++) {
    print("Hello World $i");
  }
}
